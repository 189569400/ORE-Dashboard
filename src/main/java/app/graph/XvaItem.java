package app.graph;

public class XvaItem {

    private String name;
    private Double value;

    public XvaItem(String name, Double value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public Double getValue() {
        return value;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setValue(Double value) {
        this.value = value;
    }

    public void dump() {
        System.out.println("name=" + name + " value=" + value);
    }
}

